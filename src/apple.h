#pragma once
#include <SFML/Graphics.hpp>
#include "wall.h"

// Константы размеров игрового поля в клетках
// WIDTH - ширина поля в клетках (60 клеток)
const int WIDTH = 60;
// HEIGHT - высота поля в клетках (30 клеток)
const int HEIGHT = 30;

// Класс Apple (Яблоко) - представляет собой объект яблока в игре
class Apple {
public:
    // Конструктор по умолчанию - создает яблоко с начальной позицией
    Apple();

    // Метод для получения текущей позиции яблока
    // Возвращает: вектор позиции (x, y) яблока на поле
    sf::Vector2f getPosition() const;

    // Метод для перегенерации (респавна) яблока на новом месте
    // Параметр walls: список стен для проверки пересечения
    void respawn(const std::vector<Wall>& walls);

    // Метод для проверки, находится ли яблоко на стене
    // Параметр walls: список стен для проверки
    // Возвращает: true если яблоко находится на стене, иначе false
    bool isOnWall(const std::vector<Wall>& walls) const;

private:
    // Приватное поле для хранения позиции яблока на игровом поле
    // Тип sf::Vector2f - двухмерный вектор с координатами x и y
    sf::Vector2f position;
};