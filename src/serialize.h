#pragma once
#include <string>

#include <vector>

// Функция сериализации (преобразования структурированных данных в строку)
// Назначение: преобразует вектор пар "ключ-значение" в строковый формат для сохранения
// Параметр data: вектор пар строк, где first - ключ, second - значение
// Возвращает: строку в формате "ключ1:значение1\nключ2:значение2\n..."
// Используется для сохранения настроек игры в текстовом файле
std::string serialize(std::vector< std::pair<std::string, std::string>> data);

// Функция десериализации (восстановления структурированных данных из строки)
// Назначение: преобразует строку обратно в вектор пар "ключ-значение"
// Параметр data: строка в формате "ключ:значение\nключ:значение\n..."
// Возвращает: вектор пар строк, восстановленных из строкового представления
// Используется для загрузки настроек игры из текстового файла
std::vector<std::pair<std::string, std::string>> unserialize(std::string data);